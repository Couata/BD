USE group7

CREATE VIEW view_arrets_par_itineraire AS
SELECT 
    ART.ID AS ARRET_ID,
    ART.NOM AS ARRET_NOM,
    AD.ITINERAIRE_ID,
    AD.SEQUENCE
FROM ARRET ART
JOIN ARRET_DESSERVI AD ON ART.ID = AD.ARRET_ID;

CREATE VIEW view_arrets_belges AS
SELECT *
FROM ARRET
WHERE LATITUDE BETWEEN 49.5294835476 AND 51.4750237087
  AND LONGITUDE BETWEEN 2.51357303225 AND 6.15665815596;
